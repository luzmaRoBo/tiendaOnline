    <h1>Bienvenido a Tienda Online</h1>
    <p id="inicio">
    Este proyecto es una aplicación web con un sistema de registro y login, donde los usuarios tienen diferentes roles y permisos.  
    Simula una tienda online donde los usuarios pueden añadir productos al carrito y pagarlos.<br><br>

    <strong>ADMINISTRADORES:</strong> Pueden gestionar usuarios y productos a través de un CRUD completo. Sin embargo, no pueden 
    comprar ni acceder al carrito.<br><br>

    <strong>USUARIOS NORMALES:</strong> Pueden registrarse, iniciar sesión y explorar los productos. Tienen la posibilidad de añadir 
    productos al carrito y comprarlos. Si eliminan un producto del carrito, el stock se actualiza automáticamente.<br><br>

    <strong>GESTIÓN DE DATOS:</strong> Si se borra un usuario, se eliminan también sus carritos guardados y se ajusta el stock de sus productos.
    Si se elimina un producto del carrito, vuelve a sumarse al stock automáticamente.
</p>
